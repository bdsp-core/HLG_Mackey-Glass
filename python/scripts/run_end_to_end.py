#!/usr/bin/env python
"""
End-to-end pipeline: raw H5 → preprocessing → EM → validation → figures.

Processes raw BDSP PSG recordings (H5 format at 200Hz) through the full
HLG pipeline and cross-validates against pre-computed intermediate results.

Pipeline steps:
    1. Read raw H5 (signals + annotations at 200Hz)
    2. Resample to 10Hz, map stage/resp/arousal encodings
    3. Compute ventilation trace (Ventilation_ABD, d_i_ABD, arousal_locs)
    4. Segment into 8-min NREM/REM blocks
    5. Run Python EM algorithm on each segment
    6. Cross-validate against pre-computed reference CSVs
    7. Generate figures

Data layout:
    data/raw_h5/              Raw BDSP H5 recordings
    data/paper_examples/      Pre-computed reference CSVs (for validation)
    data/pipeline_output/     Pipeline output (generated by this script)

Usage:
    python -m scripts.run_end_to_end
    python -m scripts.run_end_to_end --study 7       # Single study
    python -m scripts.run_end_to_end --skip-em        # Only preprocess
"""

